class TextProcessor(object):

    def __init__( self, textFilePath : str ):

        if not os.path.exists( textFilePath ):
            raise ValueError( 'TextProcessor - Input text file path does not exist at {}.'.format( textFilePath ) )

        self.textFilePath = textFilePath